static void fft_q15_512(int32_t *real, int32_t *imag, int inverse) {
    for (int i = 0; i < 512; i++) {
        int j = bit_rev[i];
        if (i < j) { int32_t t = real[i]; real[i] = real[j]; real[j] = t; t = imag[i]; imag[i] = imag[j]; imag[j] = t; }
    }
    for (int s = 0; s < 9; s++) {
        int m = 1 << s, m2 = m << 1;
        for (int k = 0; k < m; k++) {
            int tw = k * (512 / m2);
            int16_t wr = tw_cos[tw];
            int16_t wi = inverse ? tw_sin[tw] : (int16_t)(-tw_sin[tw]);
            for (int j = k; j < 512; j += m2) {
                int j2 = j + m;
                int32_t tr = (int32_t)(((int64_t)real[j2] * wr - (int64_t)imag[j2] * wi) >> 15);
                int32_t ti = (int32_t)(((int64_t)imag[j2] * wr + (int64_t)real[j2] * wi) >> 15);
                real[j2] = real[j] - tr; imag[j2] = imag[j] - ti;
                real[j]  += tr;         imag[j]  += ti;
            }
        }
    }
    /* IFFT: /N scaling (MATLAB ifft divides by N=512) */
    if (inverse) {
        for (int i = 0; i < 512; i++) {
            real[i] = (real[i] + 256) >> 9;
            imag[i] = (imag[i] + 256) >> 9;
        }
    }
}

static int32_t *load_i32(const char *path, int n) {
    FILE *f = fopen(path, "rb"); if (!f) return NULL;
    int32_t *b = malloc(n * sizeof(int32_t)); fread(b, sizeof(int32_t), n, f); fclose(f); return b;
}
static int16_t *load_i16(const char *path, int n) {
    FILE *f = fopen(path, "rb"); if (!f) return NULL;
    int16_t *b = malloc(n * sizeof(int16_t)); fread(b, sizeof(int16_t), n, f); fclose(f); return b;
}


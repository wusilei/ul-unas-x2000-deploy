/* STFT: Q15 fixed-point, matching MATLAB STFT_func.m with mirror padding */
int main(int argc, char **argv) {
    FILE *fwav = fopen(argv[1], "rb");
    fseek(fwav, 44, SEEK_SET);  /* skip WAV header */
    int n_samples = 160000;  /* 10s @ 16kHz */
    int16_t *x = malloc(n_samples * sizeof(int16_t));
    fread(x, sizeof(int16_t), n_samples, fwav);
    fclose(fwav);

    /* Mirror padding: [flip(x[2:257]), x, flip(x[end-256:end-1])] */
    int padded_len = PAD_LEN + n_samples + PAD_LEN;
    int16_t *xp = malloc(padded_len * sizeof(int16_t));
    /* Leading mirror: flip(x[2:257]), i.e. x[256] down to x[2] */
    for (int i = 0; i < PAD_LEN; i++) xp[i] = x[PAD_LEN - i];  /* x[256], x[255], ..., x[1] */
    /* Original signal */
    memcpy(xp + PAD_LEN, x, n_samples * sizeof(int16_t));
    /* Trailing mirror: flip(x[end-256:end-1]) */
    for (int i = 0; i < PAD_LEN; i++) xp[PAD_LEN + n_samples + i] = x[n_samples - 2 - i];

    int T = (padded_len - WIN_LEN) / WIN_INC + 1;
    
    /* Output: first 3 frames real+imag (Q20 = Q15 << 5) */
    printf("Frame,Bin,Real(Q20),Imag(Q20)\n");
    for (int t = 0; t < T && t < 3; t++) {
        int start = t * WIN_INC;

        /* Apply window: (sample * win + 16384) >> 15 → Q15 */
        int32_t fft_in[512];
        for (int i = 0; i < WIN_LEN; i++) {
            int32_t s = xp[start + i];
            fft_in[i] = (int32_t)(((int64_t)s * hann2_win[i] + 16384) >> 15);
        }

        /* Forward FFT: Q15 in, Q15 out (int32 container) */
        int32_t imag[512] = {0};
        fft_q15_512(fft_in, imag, 0);

        /* Q15 → Q20: << 5 (×32, exact, no rounding) */
        for (int b = 0; b < N_BINS; b++) {
            printf("%d,%d,%d,%d\n", t, b, fft_in[b] << 5, imag[b] << 5);
        }
    }

    /* Compute SNR vs MATLAB golden for frame 0 */
    fprintf(stderr, "Frame 0 complete. T=%d frames total.\n", T);
    free(x); free(xp);
    return 0;
}
